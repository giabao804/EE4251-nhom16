void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	//printf("vào ngắt uart");
    if (huart->Instance == USART1) { // Kiểm tra ngắt từ UART1
        char rxChar = rxBuffer[rxIndex]; // Lấy ký tự nhận được
        if (rxChar == '\n') { // Kiểm tra ký tự kết thúc chuỗi (newline)
            rxBuffer[rxIndex] = '\0';  // Thêm ký tự kết thúc chuỗi
            strcpy(command, rxBuffer); // Lưu lệnh vào bộ đệm
            rxIndex = 0;               // Reset vị trí
        } else {
            rxIndex++; // Chuyển đến vị trí tiếp theo
            if (rxIndex >= RX_BUFFER_SIZE) { // Nếu vượt quá kích thước
                rxIndex = 0; // Reset vị trí
            }
        }
        // Tiếp tục nhận ký tự tiếp theo
        HAL_UART_Receive_IT(&huart1, (uint8_t *)&rxBuffer[rxIndex], 1);
    }
}

